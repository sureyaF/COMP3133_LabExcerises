


exports.add = (x,x2) => {
    let result = 0;

    for (let i = 0; i < x; i++) {
      result=x + x2;
    }
    return result;
} 
exports.sub = (x,x2) => {
    let result = 1;

    for (let i = 0; i < x; i++) {
      result=x - x2;
    }
    return result;
} 
exports.multi = (x,x2) => {
    let result = 1;

    for (let i = 0; i < x; i++) {
      result=x * x2;
    }
    return result;
} 
exports.div = (x,x2) => {
    let result = 1;

    for (let i = 0; i < x; i++) {
      result=x / x2;
    }
    return result;
} 
